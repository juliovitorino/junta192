





import 'package:flutter/material.dart';
import 'package:junta10/br/com/jcv/linker/classes/ui/common/common-msgcode.dart';

import 'RegistroIndicacaoUI.dart';

class RegistroIndicacaoLV extends StatefulWidget {

  dynamic _pagina;

  RegistroIndicacaoLV(this._pagina);

  @override
  _RegistroIndicacaoLVState createState() => _RegistroIndicacaoLVState();
}

class _RegistroIndicacaoLVState extends State<RegistroIndicacaoLV> {
  @override
  Widget build(BuildContext context) {
    debugPrint(widget._pagina['lst'].length.toString());
    return widget._pagina['lst'].length == 0
        ? new CommonMsgCode(msgcode: "LNK-0042", msgcodeString: "Nenhum cart√£o encontrado.")
        : new ListView.builder(
      itemCount: widget._pagina['lst'].length,
      itemBuilder: (BuildContext context, int index){

        return RegistroIndicacaoUI(widget._pagina['lst'][index]);
      },
    );
  }
}




























































































































































































































































































